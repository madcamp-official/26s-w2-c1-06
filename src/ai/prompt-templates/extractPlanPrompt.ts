// "이번 프롬프트의 계획" 카드 전용: TodoWrite 없이 턴이 시작된 경우, 에이전트가
// 남긴 첫 텍스트(대개 "~을 만들겠습니다" 같은 의도 선언문)를 실제 단계별 계획으로
// 재구성한다. 새 사실을 지어내지 않고 주어진 문장에 이미 있는 내용만 재배열/요약한다.
export function buildExtractPlanPrompt(userRequest: string, intentText: string): string {
  return [
    '너는 AI 코딩 에이전트가 방금 남긴 말을 읽고, 이제부터 실제로 어떤 순서로 작업할지 계획으로 정리하는 역할이다.',
    '아래 "에이전트의 말"은 아직 진짜 계획(TodoWrite)을 세우지 않은 채로 남긴 의도 선언문이다 — 그 안에 이미 담긴 내용만 골라 2~5개의 짧은 실행 단계로 재구성해라.',
    '규칙:',
    '- 문장에 없는 내용을 새로 지어내지 마라(파일명, 함수명 등을 추측하지 마라).',
    '- 각 줄은 "- "로 시작하고, 실제로 할 작업을 동사로 끝맺는 한국어 짧은 문장으로 써라.',
    '- 다른 설명이나 인사말 없이 목록만 출력해라.',
    '- 의도 선언문이 이미 순서 없는 한 덩어리라면, 자연스러운 작업 순서로 나눠라(예: 자료구조 정의 → 핵심 로직 → 나머지 기능).',
    '사용자 요청:',
    userRequest || '(없음)',
    '에이전트의 말:',
    intentText
  ].join('\n\n')
}
